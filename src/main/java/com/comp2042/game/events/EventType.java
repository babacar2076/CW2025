package com.comp2042.game.events;

/**
 * Enumeration representing the type of movement event.
 * Defines the possible directions and actions for brick movement.
 */
public enum EventType {
    /** Move brick down */
    DOWN, 
    /** Move brick left */
    LEFT, 
    /** Move brick right */
    RIGHT, 
    /** Rotate brick counter-clockwise */
    ROTATE
}

